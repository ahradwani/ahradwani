/**
 * Lesson 1: Basic For Loop Execution Flow
 * Purpose: Iterates a set number of times and outputs increments directly to the console canvas.
 */
function SampleLoop() {
    console.log("🚀 Starting loop sequence iteration control run...");

    // Standard increment setup: Start at 1, run while i is less than or equal to 5, add 1 each step
    for (let i = 1; i <= 5; i++) {
        console.log("Iteration Number: " + i);
    }

    console.log("🏁 Loop successfully completed its cycle!");
}
